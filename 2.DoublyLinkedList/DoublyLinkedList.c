#include "DoublyLinkedList.h"

// 노드를 생성하고 초기화하는 함수
Node* DLL_CreateNode(ElementType NewData) {
    Node* NewNode = (Node*)malloc(sizeof(Node)); // 메모리 할당
    NewNode->Data = NewData; // 데이터 초기화
    NewNode->PrevNode = NULL; // 이전 노드 포인터 초기화
    NewNode->NextNode = NULL; // 다음 노드 포인터 초기화
    return NewNode;
}

// 노드를 소멸시키는 함수
void DLL_DestroyNode(Node* Node) {
    free(Node); // 메모리 해제
}

// 리스트의 끝에 새 노드를 추가하는 함수
void DLL_AppendNode(Node** Head, Node* NewNode) {
    if (*Head == NULL) {
        // 헤드가 NULL이면 새 노드를 헤드로 설정
        *Head = NewNode;
    }
    else {
        // 리스트의 마지막 노드를 찾아 새 노드를 연결
        Node* Tail = *Head;
        while (Tail->NextNode != NULL) {
            Tail = Tail->NextNode;
        }
        Tail->NextNode = NewNode;
        NewNode->PrevNode = Tail; // 새로운 노드의 PrevNode를 이전 테일로 설정
    }
}

// 특정 노드 뒤에 새 노드를 삽입하는 함수
void DLL_InsertAfter(Node* Current, Node* NewNode) {
    NewNode->NextNode = Current->NextNode; // 새로운 노드의 다음을 현재 노드의 다음으로 설정
    NewNode->PrevNode = Current; // 새로운 노드의 이전을 현재 노드로 설정
    if (Current->NextNode != NULL) {
        Current->NextNode->PrevNode = NewNode; // 다음 노드의 PrevNode를 새 노드로 설정
    }
    Current->NextNode = NewNode; // 현재 노드의 다음을 새 노드로 설정
}

// 리스트에서 특정 노드를 삭제하는 함수
void DLL_RemoveNode(Node** Head, Node* Remove) {
    if (*Head == Remove) {
        // 삭제할 노드가 헤드인 경우
        *Head = Remove->NextNode; // 헤드를 다음 노드로 변경
        if (*Head != NULL) {
            (*Head)->PrevNode = NULL; // 새로운 헤드의 PrevNode를 NULL로 설정
        }
        Remove->PrevNode = NULL;
        Remove->NextNode = NULL;
    }
    else {
        // 중간이나 마지막 노드를 삭제하는 경우
        if (Remove->NextNode != NULL) {
            Remove->PrevNode->NextNode = Remove->NextNode; // 이전 노드의 NextNode를 변경
            Remove->NextNode->PrevNode = Remove->PrevNode; // 다음 노드의 PrevNode를 변경
        }
    }
}

// 특정 위치의 노드를 반환하는 함수
Node* DLL_GetNodeAt(Node* Head, int Location) {
    Node* Current = Head;
    // 위치에 도달할 때까지 리스트를 순회
    while (Current != NULL && (--Location) >= 0) {
        Current = Current->NextNode;
    }
    return Current;
}

// 리스트의 모든 노드 개수를 세는 함수
int DLL_GetNodeCount(Node* Head) {
    int Count = 0;
    Node* Current = Head;
    // 리스트를 순회하며 노드 개수 증가
    while (Current != NULL) {
        Current = Current->NextNode;
        Count++;
    }
    return Count;
}

// 특정 노드의 데이터와 양쪽 노드의 데이터 출력
void PrintNode(Node* _Node) {
    if (_Node->PrevNode == NULL)
        printf("Prev: NULL ");
    else
        printf("Prev: %d ", _Node->PrevNode->Data);

    printf("Current: %d ", _Node->Data);

    if (_Node->NextNode == NULL)
        printf("Next: NULL\n");
    else
        printf("Next: %d\n", _Node->NextNode->Data);
}